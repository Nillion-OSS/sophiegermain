"""
Pure-Python library that provides a selection of Sophie Germain primes
that are organized by representation size.
"""
# pylint: disable=too-many-lines
from __future__ import annotations
import doctest
from typing import Iterable, Callable
import base64
import zlib

def _encode_to_ascii(prime: int) -> str: # pragma: no cover
    """
    Return the smallest possible Base64-encoded string representation of the
    supplied prime (compressing when doing so is an advantage).
    """
    bs: bytes = prime.to_bytes((prime.bit_length() // 8) + 1, 'little')
    return (
        min(
            (
                base64.standard_b64encode(zlib.compress(bs, level))
                for level in range(10)
            ),
            key=len
        )
        if prime.bit_length() >= 120 else # Compress only larger values.
        base64.standard_b64encode(bs)
    ).decode()

def _decode_from_ascii(encoding: str, bit_length: int) -> int:
    """
    Decode a prime (avoiding decompression for uncompressed values).
    """
    bs: bytes = base64.standard_b64decode(encoding)
    return int.from_bytes(
        (zlib.decompress(bs) if bit_length >= 120 else bs),
        'little'
    )

def _data(isprime: Callable[[int], bool]) -> Iterable[str]: # pragma: no cover
    """
    Generate the entries in the :obj:`_DATA` constant. This function is
    included to enable easy reproducibility.

    .. |isprime| replace:: ``isprime``
    .. isprime: https://docs.sympy.org/latest/modules/ntheory.html#sympy.ntheory.primetest.isprime

    The data in this library was generated using this function,
    with the ``isprime`` parameter instantiated to the |isprime|_ function
    found in the `SymPy <https://www.sympy.org/en/index.html>`__ library.
    """
    # Generate the smallest Sophie Germain prime for each possible binary
    # representation length.
    for bit_length in range(2, 2048 + 1):

        # Check if the candidate is a Sophie Germain prime.
        candidate = (
            (2 ** (bit_length - 1)) + 1
            if bit_length > 2 else
            2 # Smallest Sophie Germain prime with a two-bit representation.
        )
        while not (isprime(candidate) and isprime((2 * candidate) + 1)):
            candidate += 1

        # For each entry, yield its smallest Base64 (compressed when advantageous)
        # encoding.
        yield _encode_to_ascii(candidate)

