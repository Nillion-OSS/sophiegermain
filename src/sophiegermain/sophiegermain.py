"""
List containing the smallest Sophie Germain prime (encoded) for each bit length
from ``2`` to ``2049``.
"""

def sophiegermain(bit_length: int) -> int:
    """
    Return the smallest
    `Sophie Germain prime <https://en.wikipedia.org/wiki/Safe_and_Sophie_Germain_primes>`__
    the representation of which requires the specified number of bits.
    
    >>> sophiegermain(2)
    2
    >>> sophiegermain(8)
    131
    >>> sophiegermain(16)
    32771
    >>> sophiegermain(32)
    2147483693
    >>> sophiegermain(257)
    115792089237316195423570985008687907853269984665640564039457584007913129658411
    >>> sophiegermain(1025).bit_length()
    1025
    >>> sophiegermain(2049).bit_length()
    2049

    The result returned by :obj:`sophiegermain` can be represented using the
    specified number of bits.
    
    >>> all([sophiegermain(k).bit_length() == k for k in range(2, 2050)])
    True

    If a bit length outside the supported range is supplied, an
    exception is raised.

    >>> sophiegermain(-1)
    Traceback (most recent call last):
      ...
    ValueError: all Sophie Germain primes have bit lengths of at least 2
    >>> sophiegermain(1)
    Traceback (most recent call last):
      ...
    ValueError: all Sophie Germain primes have bit lengths of at least 2
    >>> sophiegermain(2050)
    Traceback (most recent call last):
      ...
    ValueError: bit lengths of 2050 and greater are not supported

    The supplied bit length must be an integer.
    
    >>> sophiegermain('abc')
    Traceback (most recent call last):
      ...
    TypeError: bit length must be an integer
    """
    if not isinstance(bit_length, int):
        raise TypeError('bit length must be an integer')

    if bit_length < 2:
        raise ValueError('all Sophie Germain primes have bit lengths of at least 2')

    if bit_length > len(_DATA) + 1:
        raise ValueError(
            'bit lengths of ' + str(len(_DATA) + 2) +' and greater are not supported'
        )

    return _decode_from_ascii(_DATA[bit_length - 2], bit_length)

